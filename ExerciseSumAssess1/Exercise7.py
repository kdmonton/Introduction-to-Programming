#0 to 50 increments of 1
print('Count 0 to 50:')
for a in range(0,51):
    print (a, end=' ')

#50 to 0 increments of 1
print('Count 50 to 0:')
for a in range(50,-1,-1):
    print(a, end=" ")

#30 to 50 increments of 1
print ('Count 30 to 50:')
for a in range(30,51):
    print(a, end=' ')

#50 to 10 increments of 2
print ('Count 50 to 10:')
for a in range(50,9,-2):
    print(a, end=" ")

#100 to 500 increments of 5
print ('Count 100 to 200:')
